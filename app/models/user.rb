class User < ActiveRecord::Base
  has_many :notes
  has_many :questions

  validates :email,
            presence: true,
            uniqueness: { case_sensitive: false },
            format: { with: /\A[\w\-\.]+@[\w\-\.]+\Z/, message: "must be a valid email address" }

  validates :name, presence: true

  has_secure_password
end
